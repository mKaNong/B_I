function getAreaRealName(name){
	
	
	if(name == '黑龙江'){
		name ='黑龙江省';
	}
	if(name == '西藏'){
		name ='西藏自治区';
	}
	if(name == '内蒙古'){
		name ='内蒙古自治区';
	}
	if(name == '广西'){
		name ='广西壮族自治区';
	}
	if(name == '宁夏'){
		name ='宁夏回族自治区';
	}
	if(name == '香港'){
		name ='香港特别行政区';
	}
	if(name == '澳门'){
		name ='澳门特别行政区';
	}
	if(name == '新疆'){
		name ='新疆维吾尔自治区';
	}
	
	
	return name;
}